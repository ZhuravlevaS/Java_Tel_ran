package lessons;

public class DataTypes {
    public static void main(String[] args) {
        // Primitive
        byte a = 10; //1 byte (-128 до 127)
        short s = 500; //2 byte (-30 000 до + 30 000)
        int w = 1100000; // 4 byte (-2 000 000 000 до + 2 000 000 000)
        long l = 3000000000L; // 8 byte (-9 10^18 до +9 10^18)

        float f = 1235000.123f; //4 byte
        double g = 123000000000000.123; //8 byte

        char c = '1'; //2 byte
        boolean bool = false; //1 byte
    }
}
